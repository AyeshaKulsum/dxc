
public class Student {

	String name;
	int regno;
	public Student(String name, int regno) {
		
		this.name = name;
		this.regno = regno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRegNo() {
		return regno;
	}
	public void setRegNo(int regno) {
		this.regno = regno;
	}
	
	
}
